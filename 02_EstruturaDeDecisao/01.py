"""
Faça um Programa que peça dois números e imprima o maior deles.
"""

num_1 = float(input("Digite um número: "))
num_2 = float(input("Digite outro número: "))

if num_1 > num_2:
    print(f"O primeiro número digitado: {num_1} é maior que o segundo: {num_2}")
else:
    print(f"O segundo número digitado: {num_2} é maior que o primeiro: {num_2}")
